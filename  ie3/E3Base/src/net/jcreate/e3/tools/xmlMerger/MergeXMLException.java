package net.jcreate.e3.tools.xmlMerger;

public class MergeXMLException extends RuntimeException{

	private static final long serialVersionUID = 1L;
	

	public MergeXMLException() {
		super();
	}

	public MergeXMLException(String message, Throwable cause) {
		super(message, cause);
	}

	public MergeXMLException(String message) {
		super(message);
	}

	public MergeXMLException(Throwable cause) {
		super(cause);
	}
}
